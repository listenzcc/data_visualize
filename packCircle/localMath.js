// Useful math functions
// Triangle functions by angles.
function sin(x) {
    return Math.sin((x / 180) * Math.PI);
}

function cos(x) {
    return Math.cos((x / 180) * Math.PI);
}

function asin(x) {
    return (Math.asin(x) / Math.PI) * 180;
}

function acos(x) {
    return (Math.acos(x) / Math.PI) * 180;
}
